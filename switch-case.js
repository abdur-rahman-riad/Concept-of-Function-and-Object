var color = 'red';
switch (color) {
    case 'blue':
        console.log("Color is Blue");
        break;
    case 'green':
        console.log("Color is Green");
        break;
    case 'white':
        console.log("Color is White");
        break;
    case 'red':
    case 'crimson':
        console.log("Color is Red/Crimson");
        break;
    default:
        console.log("Color is Black");
}